public class ArithmaticOperator {
	public static void main (String[]args) {
		int num1 = 10;
		int num2 = 5;
		int add = num1+num2;
		int sub = num1-num2;
		int multi = num1*num2;
		int division = num1/num2;
		int modulus = num1%num2;
		int avg = add/2;
		int square = num1*num1;
		int cube = num2*num2*num2;
			
				
		System.out.println("Add="+add + "Sub="+sub + "Multi="+multi + "Div="+division + "Modulus="+modulus + "Avg="+avg + "Square="+square + "Cube="+cube);
		
				
	}

}
